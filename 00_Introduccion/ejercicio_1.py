# Introducción
# Una expresión puede ser una operación aritmetica

print(6 + 9)                       # 15
print(3 - 1)                       # 2
print(1 * 1)                       # 1
print(8 / 5)                       # 1.6
print(2 ** 4)                      # 16
print(8 % 3)                       # 2
print(9 // 4)                      # 2
print(15<6)                        # False
print(2.5 * 1.5 * 3)               # 11.25
print(2j + 2 + 3j + 5)             # (7+5j)
print('Hola mundo cruel')          # Hola mundo cruel
print(['Marea', 2, 5])             # ['Marea', 2, 5]
print({'Nombre':'Tu nombre'})      # {'Nombre': 'Tu nombre'}
print({'aaah', 2.67, 9.5})         # {9.5, 2.67, 'aaah'}
print(('Lag', 6.8, 7.5, 9))        # ('Lag', 6.8, 7.5, 9)

# Tipos de datos

print(type(15<6))                       # <class 'bool'>
print(type(2.5 * 1.5 * 3))              # <class 'float'>
print(type(2j + 2 + 3j + 5))            # <class 'complex'>
print(type('Hola mundo cruel'))         # <class 'str'>
print(type(['Marea', 2, 5]))            # <class 'list'>
print(type({'Nombre':'Tu nombre'}))     # <class 'dict'>
print(type({'aaah', 2.67, 9.5}))        # <class 'set'>
print(type(('Lag', 6.8, 7.5, 9)))       # <class 'tuple'>
